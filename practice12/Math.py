class Math:
    def __init__(self, a: int, b: int):
        self.a = a
        self.b = b

    def addition(self):
        print(self.a + self.b)

    def multi(self):
        print(self.a * self.b)

    def div(self):
        print(self.a / self.b)

    def subtraction(self):
        print(self.a - self.b)